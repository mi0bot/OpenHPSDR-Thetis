namespace Thetis
{
    public class TuneStep
    {
        private int step_hz;
        public int StepHz
        {
            get { return step_hz; }
        }

        private string name;
        public string Name
        {
            get { return name; }
        }

        public TuneStep(int _step_hz, string _name)
        {
            step_hz = _step_hz;
            name = _name;
        }

        public override string ToString()
        {
            return name;
        }
    }
}
